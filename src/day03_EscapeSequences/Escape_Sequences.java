package day03_EscapeSequences;

/*
escape sequences:Must be given with double quates
\n: start a new line
\t: paragraph space(tab)
\\:  single back slash
\": double quate



 */


public class Escape_Sequences {

    public static void main(String[] args) {
        System.out.println("Java \nPython \n C#");

        System.out.println("------------------");

        System.out.println("Hello Cydeo \nHow are you all today ?\nIt's nice to see you all !\nWhat class do we have next week");

        System.out.println("-----------------------");

        System.out.println("\tJava is Cool Programming Language");
        System.out.println("----------------");

        System.out.println("/\\"); // in order to print 1 back slash we need to put 2 back slash

        System.out.println("------------");

        System.out.println("My favorite movie is \"Game of Throne\""); // in ordert to print double code we have to use the back slash



    }





}
