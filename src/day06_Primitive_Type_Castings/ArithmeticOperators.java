package day06_Primitive_Type_Castings;

public class ArithmeticOperators {
    public static void main(String[] args) {
        System.out.println(10+20); //addition  30
        System.out.println(100-50);//subtraction  50
        System.out.println("12" + 1); // concatenation 121
        System.out.println(10*6);  //  multiplication  60

        System.out.println( 100 / 3);
        System.out.println(10.0/4);
        System.out.println(10/4.0);
        System.out.println(10d/4);

        int a = 100;
        double b = a/6;
        System.out.println(b);

        double c = a/6d;
        System.out.println(c);
        System.out.println(100D);

        System.out.println(10%4);
        System.out.println(20%5);

        System.out.println(20%6);









    }

}
/*
+ : Addition
- : Subtraction
/ : Division
* : Multiplication
% : Modulus
 */